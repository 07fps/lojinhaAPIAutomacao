package pojo;

public class ComponentePojo {
    private String componenteNome;
    private int compomenteQuatidade;

    public String getComponenteNome() {
        return componenteNome;
    }

    public void setComponenteNome(String componenteNome) {
        this.componenteNome = componenteNome;
    }

    public int getCompomenteQuatidade() {
        return compomenteQuatidade;
    }

    public void setCompomenteQuatidade(int compomenteQuatidade) {
        this.compomenteQuatidade = compomenteQuatidade;
    }
}
